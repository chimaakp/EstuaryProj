package enums;
/**
 * The power-ups available to the user if they correctly answer power up questions
 * @author Zeke Faison
 *
 */
public enum PowerUps {
	/**
	 * The player will have a helper who will show all of the available eggs for a time period
	 */
	HELPER,
	/**
	 * The player's bird will grow and devour every egg on the board
	 */
	DEVOUR,
	/**
	 * Some trash will be cleared from the board
	 */
	CLEANER,
	/**
	 * Time will stop for 5 seconds
	 */
	FREEZE,
	/**
	 * The player will get double eggs
	 */
	BONUS;
	

}
